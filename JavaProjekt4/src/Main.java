public class Main {
    public static void main(String[] args) {
        // operators + - * / %
        int result = 2;
        System.out.println(result);
        result++;
        System.out.println(result);
        result--;
        System.out.println(result);
        result += 12;
        System.out.println(result);
        result -= 10;
        System.out.println(result);
        result *= 3;
        System.out.println(result);
        result /= 3;
        System.out.println(result);
    }
}